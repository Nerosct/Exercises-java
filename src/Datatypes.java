
public class Datatypes {
    public static void main(String[] args){
        int hello_world = 5; // declaring an integer
        double num2 = 5.0;  //declaring a floating point
        boolean  crash = false; // declaring a boolean
        char c = '5';
        String name = "juliano";

        int tim = hello_world; // making a variable receive the value of another variable

        System.out.println(tim);
        System.out.println(hello_world);
        System.out.println(num2);
        System.out.println(crash);
        System.out.println(c);
        System.out.println(name);
    }
}
